-- ================================================================
-- DDL: Create base DB2 tables for reconciliation and auditing.
-- ================================================================

CREATE TABLE FIN_ACCOUNT (
    ACCOUNT_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ACCOUNT_NUMBER VARCHAR(20) NOT NULL,
    ACCOUNT_HOLDER VARCHAR(100) NOT NULL,
    ACCOUNT_STATUS CHAR(1) NOT NULL,
    CURRENCY CHAR(3) NOT NULL,
    OPEN_DATE DATE NOT NULL
);

CREATE TABLE FIN_MOVEMENT (
    MOVEMENT_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TRANSACTION_ID VARCHAR(20) NOT NULL,
    ACCOUNT_ID BIGINT NOT NULL,
    MOVEMENT_DATE DATE NOT NULL,
    MOVEMENT_TYPE CHAR(2) NOT NULL,
    AMOUNT DECIMAL(13,2) NOT NULL,
    CURRENCY CHAR(3) NOT NULL,
    REFERENCE VARCHAR(35),
    CHANNEL VARCHAR(10),
    SOURCE_SYSTEM VARCHAR(20),
    LOAD_TIMESTAMP TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);

CREATE TABLE FIN_STG_MOVEMENT (
    STG_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TRANSACTION_ID VARCHAR(20) NOT NULL,
    TRANSACTION_DATE DATE NOT NULL,
    ACCOUNT_NUMBER VARCHAR(20) NOT NULL,
    ACCOUNT_ID BIGINT,
    TRANSACTION_TYPE CHAR(2) NOT NULL,
    AMOUNT DECIMAL(13,2) NOT NULL,
    CURRENCY CHAR(3) NOT NULL,
    REFERENCE VARCHAR(35),
    CHANNEL VARCHAR(10),
    LOAD_TIMESTAMP TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    PROCESS_STATUS CHAR(1) DEFAULT 'N' NOT NULL,
    RECON_STATUS VARCHAR(10) DEFAULT 'PENDING',
    ERROR_CODE VARCHAR(6),
    ERROR_DESCRIPTION VARCHAR(60),
    RECON_DATE DATE,
    RECON_TIMESTAMP TIMESTAMP
);

CREATE TABLE FIN_RECON_CONTROL (
    CTRL_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    STG_ID BIGINT NOT NULL,
    TRANSACTION_ID VARCHAR(20) NOT NULL,
    ACCOUNT_ID BIGINT,
    ACCOUNT_NUMBER VARCHAR(20) NOT NULL,
    STATUS VARCHAR(10) NOT NULL,
    DIFFERENCE_AMOUNT DECIMAL(13,2) NOT NULL,
    RECON_DATE DATE NOT NULL,
    REMARKS VARCHAR(60),
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);

CREATE TABLE FIN_DISCREPANCY (
    DISC_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    STG_ID BIGINT NOT NULL,
    TRANSACTION_ID VARCHAR(20) NOT NULL,
    ACCOUNT_ID BIGINT,
    ACCOUNT_NUMBER VARCHAR(20) NOT NULL,
    ERROR_CODE VARCHAR(6) NOT NULL,
    ERROR_DESCRIPTION VARCHAR(60) NOT NULL,
    EXPECTED_AMOUNT DECIMAL(13,2) NOT NULL,
    REGISTERED_AMOUNT DECIMAL(13,2) NOT NULL,
    DISC_DATE DATE NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);

CREATE TABLE FIN_AUDIT_LOG (
    AUDIT_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PROCESS_NAME VARCHAR(30) NOT NULL,
    STEP_NAME VARCHAR(30) NOT NULL,
    ENTITY_NAME VARCHAR(30),
    ENTITY_ID VARCHAR(50),
    EVENT_TYPE VARCHAR(20) NOT NULL,
    EVENT_STATUS VARCHAR(10) NOT NULL,
    MESSAGE_TEXT VARCHAR(200),
    EVENT_TIMESTAMP TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);
